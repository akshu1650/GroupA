package HomeWork;

public class DefaultConstructor {

	public void multiplication()
	
	{
		int a=3;
		int b=5;
		int c=a*b;
		System.out.println("multiplication is "+c);
	}
	
	
	
	
	public static void main(String[] args) 
	{
		DefaultConstructor dc=new DefaultConstructor();
		dc.multiplication();

	}

}
