package Loops;

public class For_Loop_Study {

	public static void main(String[] args) {
		
		// I want to print 0-10 & increement by 1
		
		for(int a=0; a<=10; a++)// initialization, condition, updation//a=0,1,2,3...10 
		{
			System.out.println(a);//0,1,2,3,4,..10
			
		}
			System.out.println("==");

		// I want to print 2's table
			
		for(int a=2; a<=20; a=a+2)
		{
			System.out.println(a);
		}
			System.out.println("==");
			
		// I want to print 10-0 & decrement by 1
			
		for(int a=10; a>=0; a--)
		{
			System.out.println(a);
		}
			System.out.println("==");
			
		// I want to print 5's table 
			
		for (int a=5; a<=50; a=a+5)
		{
			System.out.println(a);
		}
			System.out.println("==");
	
		// I want to print 5's table in reverse order
		
		for(int a=50; a>=5; a=a-5)
		{
			System.out.println(a);
		}
	
			System.out.println("==");
		
		// I Want to print VELOCITY 5 times
		
		for(int a=1; a<=5; a++)
		{
			System.out.println("VELOCITY");
		}
	
	
	
		
	
	}
}