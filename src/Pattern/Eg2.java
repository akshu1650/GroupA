package Pattern;

public class Eg2 {

	public static void main(String[] args) {
		// * * * * *
		// * * * * *  
		// * * * * *
		// * * * * *
		// * * * * *
	
	// row-5 col-5 
		
		for(int i=1; i<=5; i++)// outer loop for Row
		{
			for(int j=1; j<=5; j++)// inner loop for col
			{
			
				System.out.print("* ");
		}
	
			System.out.println();
	
	
	
	}	

	}
}
