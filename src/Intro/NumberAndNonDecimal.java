package Intro;

public class NumberAndNonDecimal {

	public static void main(String[] args) {
		
		//1. Variable declaration
		
		byte number;
		short num1;
		int roll1;
		long rollNum;
		
		
		//2. Variable initialization
		
		number=100;
		num1=30000;
		roll1=2000000000;
		rollNum=800;
		
		
		
		
				
		//3. Usage
		
		System.out.println("Value of number is "+number);
		System.out.println("Value of num1 is "+num1);
		System.out.println("Value of roll1 is "+roll1);
		System.out.println("Value of rollNum is "+rollNum);
		
		
	}

}
